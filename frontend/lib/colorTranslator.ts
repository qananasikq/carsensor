const COLOR_SUFFIX_NORMALIZERS: [RegExp, string][] = [
  [/\s+/g, ' '],
  [/\s*\/\s*/g, ' / '],
  [/\s*・\s*/g, ' / '],
  [/\s*\+\s*/g, ' / '],
  [/\s*×\s*/g, ' / '],
  [/\s*x\s*/gi, ' / '],
  [/\bII\b/g, ''],
  [/\bIII\b/g, ''],
  [/\bIV\b/g, ''],
  [/\bV\b/g, ''],
  [/\s{2,}/g, ' ']
];

function replaceAllByMap(text: string, map: Record<string, string>): string {
  let result = String(text || '');
  const replacements = Object.entries(map).sort((a, b) => b[0].length - a[0].length);

  for (const [from, to] of replacements) {
    if (!from || !to) continue;
    result = result.split(from).join(to);
  }

  return result;
}

function cleanupColorText(value: unknown): string {
  let text = String(value || '').normalize('NFKC').trim();

  for (const [pattern, replacement] of COLOR_SUFFIX_NORMALIZERS) {
    text = text.replace(pattern, replacement);
  }

  return text
    .replace(/[()（）【】「」『』]/g, ' ')
    .replace(/\s{2,}/g, ' ')
    .replace(/(^[\s/,-]+|[\s/,-]+$)/g, '')
    .trim();
}

export function translateColor(value: unknown): string {
  const source = cleanupColorText(value);
  if (!source) return '';

  const translated = cleanupColorText(
    replaceAllByMap(source, colorDictionary)
  );
  return translated;
}

